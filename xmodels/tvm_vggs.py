def VGGNets(arch, model_path=None):
    pretrained = True if model_path else False

    if arch == 'vgg19':
        return vgg19(pretrained, model_path)
    elif arch == 'vgg16':
        return vgg16(pretrained, model_path)
    elif arch == 'vgg19_bn':
        return vgg19_bn(pretrained)
    else:
        raise NotImplementedError
